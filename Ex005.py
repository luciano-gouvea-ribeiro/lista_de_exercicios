""""
 005: Faça um programa que leia um número Inteiro e mostre na tela o
seu sucessor e seu antecessor.

"""
numero = int(input('Digite um numero: '))
sucessor = numero + 1
antecessor = numero - 1

print(
    f'Analisando o numero: {numero}, o seu antecessor é {antecessor}, e o sucessor é {sucessor}'
)

